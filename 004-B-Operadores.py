#Operadores de atribuição Python
#Operadores de atribuição são usados ​​para atribuir valores a variáveis:

#Atribuir valor (=)	x = 5	x = 5	
#Incrementar Valor (+=)	x += 3	x = x + 3	
#Decrementar Valor (-=)	x -= 3	x = x - 3	
#Multiplicar valor (*=)	x *= 3	x = x * 3	
#Dividir valor (/=)	x /= 3	x = x / 3	
#modulo do valor (%=) x %= 3	x = x % 3	(RESTO DA DIVISÃO)
#Floor division (//=)	x //= 3	x = x // 3	(RESULTADO DA DIVISÃO EXATO, SEM CASAS DECIMAIS)

############
#**=	x **= 3	x = x ** 3	
#&=	x &= 3	x = x & 3	
#|=	x |= 3	x = x | 3	
#^=	x ^= 3	x = x ^ 3	
#>>=	x >>= 3	x = x >> 3	
#<<=	x <<= 3	x = x << 3

###############################
