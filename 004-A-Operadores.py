#Operadores aritméticos Python
#Operadores aritméticos são usados ​​com valores numéricos para realizar operações matemáticas comuns:
# +	Addition	x + y	
# -	Subtraction	x - y	
# *	Multiplication	x * y	
# /	Division	x / y	
# %	Modulus	x % y	(RESTO DA DIVISÃO)
# **	Exponentiation	x ** y	
# //	Floor division	x // y (RESULTADO DA DIVISÃO EXATO, SEM CASAS DECIMAIS)

