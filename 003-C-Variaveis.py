#Variaveis do tipo texto (String)
#As strings em python são colocadas entre aspas simples ou aspas duplas.
#'hello' é o mesmo que "hello" .

#Você pode exibir um literal de string com a print()função:

print("Bom dia")

print('Boa noite')


conceito = "positivo"

tipoSangue = "O"

familia = 'silva'

print(conceito)
print(tipoSangue)
print(familia)

#Você pode usar 3 aspas duplas 
a = """Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt
ut labore et dolore magna aliqua."""
print(a)


#Você pode usar 3 aspas simples
a = '''Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt
ut labore et dolore magna aliqua.'''
print(a)

#Concatenar = Unir
#Usamos o sinal de + para unir duas variaveis do tipo texto
print(conceito + tipoSangue)